#include <unistd.h>

int	main(int argc, char const *argv[])
{
	int	i;
	int	j;

	argc += 0;
	j = 0;
	i = 1;
	while (argv[i])
	{
		while (argv[i][j])
		{
			write(1, &argv[i][j], 1);
			j++;
		}
		write(1, "\n", 1);
		j = 0;
		i++;
	}
	return (0);
}
